m, n = map(int, input().split())
Movie = []

for i in range(n):
    Movie.append(list(map(int, input().split()))) # 0 -> æ—¶é•¿ # 1 -> åˆ©æ¶¦
k = int(input())
for i in range(n):
    Movie[i][0] += k
